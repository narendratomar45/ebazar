const mongoose = require("mongoose");

const registerSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Registration = mongoose.model("Registration", registerSchema);
module.exports = Registration;
